const parseTile = (tile) => {
  let [id, ...lines] = tile.split("\n")
  id = Number(id.replace(/Tile (.*):/, "$1"))
  return { id, lines }
}

const parse = (input) => {
  const tiles = input.trim().split("\n\n").map(parseTile)
  return tiles
}

