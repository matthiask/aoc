const forest = input.split("\n").map((line) => Array.from(line).map((character) => character === "#"))
const modX = forest[0].length
const maxY = forest.length

// console.log(forest)

function hasTreeAt(x, y) {
  return forest[y][x % modX]
}

function count(xv, yv) {
  let encountered = 0
  for (let i=0;; ++i) {
    if (i * yv >= maxY) {
      break
    }
    if (hasTreeAt(i * xv, i * yv)) {
      ++encountered
    }
  }
  return encountered
}

